public abstract class FichaCadastral{
    String cadastroNome;
    String cadastroID;

    public String getCadastroNome() {
        return cadastroNome;
    }

    public void setCadastroNome(String cadastroNome) {
        this.cadastroNome = cadastroNome;
    }

    public String getCadastroID() {
        return cadastroID;
    }

    public void setCadastroID(String cadastroID) {
        this.cadastroID = cadastroID;
    }
}
